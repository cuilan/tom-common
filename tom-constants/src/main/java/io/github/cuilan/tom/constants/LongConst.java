package io.github.cuilan.tom.constants;

/**
 * Long类型值常量
 *
 * @author zhangyan
 * @since 2021/12/7
 */
public class LongConst {

    /**
     * long整数-1
     */
    public static final long INT_NEG_ONE = -1L;

    /**
     * long整数0
     */
    public static final long LONG_ZERO = 0L;

    /**
     * long整数1
     */
    public static final long LONG_ONE = 1L;

    /**
     * long整数10
     */
    public static final long LONG_TEN = 10L;

    /**
     * 默认起始分页
     */
    public static final long DEFAULT_PAGE_NUM = LONG_ONE;

    /**
     * 默认分页大小
     */
    public static final long DEFAULT_PAGE_SIZE = LONG_TEN;

    /**
     * 1毫秒
     */
    public static final Long MILLISECOND = LONG_ONE;

    /**
     * 1秒
     */
    public static final Long SECOND = MILLISECOND * 1000;

    /**
     * 1分钟
     */
    public static final Long MINUTE = SECOND * 60;

    /**
     * 1小时
     */
    public static final Long HOUR = MINUTE * 60;

    /**
     * 1天
     */
    public static final Long DAY = HOUR * 24;

    /**
     * 1周
     */
    public static final Long WEEK = DAY * 7;

}
