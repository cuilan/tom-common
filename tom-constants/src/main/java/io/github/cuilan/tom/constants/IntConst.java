package io.github.cuilan.tom.constants;

/**
 * Integer类型值常量
 *
 * @author zhangyan
 * @since 2021/12/7
 */
public class IntConst {

    /**
     * int整数-1
     */
    public static final int INT_NEG_ONE = -1;

    /**
     * int整数0
     */
    public static final int INT_ZERO = 0;

    /**
     * int整数1
     */
    public static final int INT_ONE = 1;

    /**
     * int整数10
     */
    public static final int INT_TEN = 10;

    /**
     * 默认起始分页
     */
    public static final int DEFAULT_PAGE_NUM = INT_ONE;

    /**
     * 默认分页大小
     */
    public static final int DEFAULT_PAGE_SIZE = INT_TEN;

    /**
     * 1毫秒
     */
    public static final Integer MILLISECOND = INT_ONE;

    /**
     * 1秒
     */
    public static final Integer SECOND = MILLISECOND * 1000;

    /**
     * 1分钟
     */
    public static final Integer MINUTE = SECOND * 60;

    /**
     * 1小时
     */
    public static final Integer HOUR = MINUTE * 60;

    /**
     * 1天
     */
    public static final Integer DAY = HOUR * 24;

    /**
     * 1周
     */
    public static final Integer WEEK = DAY * 7;

}
